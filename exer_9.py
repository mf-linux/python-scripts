x, y, z = 5, 1, 0

x += y                      # x = 5 + 1 -> x = 6
x -= y                      # x = 6 - 1 -> x = 5    
x *= (x + y)                # x = 5 * (5 + 1) -> x = 5 * 6 -> x = 30
y *= 5                      # y = 1 * 5 -> y = 5
x //= y                     # x = 30 / 5 -> x = 6
z = x                       # z = 6
x **= 2                     # x = 6 ^ 2 -> x = 36
x %= y                      # x = 36 % 5 -> x = 1
