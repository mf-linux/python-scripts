x, y = 6, 5

## x é igual a y ?
print(x == y)
print(x.__eq__(y))

print('')

## x é maior que a y ?
print(x > y)
print(x.__gt__(y))

print('')

## x é maior ou igual a y ?
print(x >= y)
print(x.__ge__(y))

print('')

## x é menor que y ?
print(x < y)
print(x.__lt__(y))

print('')

## x é menor ou igual a y?
print(x <= y)
print(x.__le__(y))

print('')

## x é diferente de y?
print(x != y)
print(x.__ne__(y))